<?php 
    /**
     * Boolean
     *  --> true & false
     *  --> true - "1"
     *  --> false - ""
     */

     /**
      * Comparison Operators:
      *     --> Greater & Less Than Operator Works on Strings Alphabetically.
      *     --> '==', '===', '<', '>', '<=', '>=', '!='
      */
      echo 5 < 10;
      echo 10 > 5;
      echo 5 <= 10;
      echo 10 >= 5;
      echo 10 == 10;
      echo 5 == 10;
      echo 5 != 10;
      
    //   Loose Equal Comparision - Only Check Values Not Data Type.
    echo 5 == "5";

    // Strict Equal Comparision - Check Values as well as Data Type.
    echo 5 === "5";

    /**
     * Logical Operators
     *  --> '&&' - AND Operator
     *  --> '||' - OR Operator
     *  --> '!' - NOT Operator
     */
      
?>